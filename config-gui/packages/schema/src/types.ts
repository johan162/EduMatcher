/**
 * Canonical client/server data model for the config builder.
 *
 * `EngineConfigDraft` mirrors the structure of `engine_config.yaml` directly
 * (see design §13) rather than the CLI flag surface. The CLI mini-languages are
 * strictly an input-encoding concern that the GUI avoids by editing structured
 * fields. Enum unions below mirror the Python engine models in
 * `src/edumatcher/models/`.
 */

